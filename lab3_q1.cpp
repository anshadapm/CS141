//Library
#include <iostream>
using namespace std;
int main(){
   //declaring variables of all types
   int a = 2;/*integer*/
   char b = 'x';/*charecter*/
   bool c = true;/*boolean*/
   float d = 0.3;/*float*/
   double e = 1.99;/*double*/
    //declaration of output
   cout<< "a = "<< a << endl;
   cout<< "b = "<< b << endl;
   cout<< "c = "<< c << endl;
   cout<< "d = "<< d << endl;
   cout<< "e = "<< e << endl;
   //displaying the size of data types
   cout<< "size of integer " << sizeof(a) << endl;
   cout<< "size of charecter " << sizeof(b) << endl;
   cout<< "size of boolean " << sizeof(c) << endl;
   cout<< "size of float " << sizeof(d) << endl;
   cout<< "size of double " << sizeof(e) << endl;
}
        
